// Creating KmToMiles class to convert 10.8 km to miles
class KmToMiles {
    public static void main(String[] args) {

        // Creating a variables
        double km = 10.8;
        double miles = km * 1.6;

        // Printing the converted distance
        System.out.println("The distance " + km + " km in miles is " + miles);
    }
}