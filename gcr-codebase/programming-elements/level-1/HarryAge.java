// Creating HarryAge class to compute Harry's age
class HarryAge {
    public static void main(String[] args) {

        // Creating variables for birth year and current year
        int birthYear = 2000;
        int currentYear = 2024;

        // Printing Harry's age
        System.out.println("Harry's age in 2024 is " + (currentYear - birthYear));
    }
}